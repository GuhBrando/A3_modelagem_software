package br.com.anhembi.a3;

public class Estudantes {

	private int id;
	private String nome;
	private String sobrenome;
	private int registroEstudante;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public String getSobrenome() {
		return sobrenome;
	}
	public void setSobrenome(String sobrenome) {
		this.sobrenome = sobrenome;
	}
	public int getRegistroEstudante() {
		return registroEstudante;
	}
	public void setRegistroEstudante(int registroEstudante) {
		this.registroEstudante = registroEstudante;
	}
}
